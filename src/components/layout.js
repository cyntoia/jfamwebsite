import React from 'react'
import PropTypes from 'prop-types'
import Helmet from 'react-helmet'
import { StaticQuery, graphql } from 'gatsby'

import '../assets/scss/main.scss'

const Layout = ({ children, location }) => {

  let content;

  if (location && location.pathname === '/') {
    content = (
      <div>
        {children}
      </div>
    )
  } else {
    content = (
      <div id="wrapper" className="page">
        <div>
          {children}
        </div>
      </div>
    )
  }

  return (
    <StaticQuery
      query={graphql`
        query SiteTitleQuery {
          site {
            siteMetadata {
              title
            }
          }
        }
      `}
      render={data => (
        <>
          <Helmet
            title={data.site.siteMetadata.title}
            meta={[
              { name: 'description', content: 'The JFAM Foundation is dedicated to supporting and amplifying often silenced narratives relating to social and criminal justice. We believe that every testimony is one worth telling and inside every journey is a lesson to be shared.' },
              { name: 'keywords', content: 'justice,freedom,mercy,prison,reform,prison reform,cyntoia,cyntoia brown,cyntoia brown long,nonprofit,google,bing,yahoo,tennessee,prisoner,sex,sex trafficking,human trafficking,domestic minor sex trafficking,foundation for justice freedom mercy,kardashian,beyonce,kylie,donate,philanthropy,search' },
            ]}
          >
            <html lang="en" />
          </Helmet>
          {content}
        </>
      )}
    />
  )
}

Layout.propTypes = {
  children: PropTypes.node.isRequired,
}

export default Layout
